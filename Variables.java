public class Variables{

  public static void main(String[] args){

    // Primitive Data TYPES :------
      // byte - 1 byte memory (-128 to 127)
      // short - 2 bytes memory (-32768 to 32767)
      // int - 4 bytes memory (-2147483648 to 2147483647)
      // long - 8 bytes memory (-9223372036854775808 to 9223372036854775807)
      // float - 4 bytes memory 
      // double - 8 bytes memory 
      // char - 2 bytes memory    a,b,c,@, # 
      // boolean - 1 byte memory  true/false 

      byte a = 4 ; 
      System.out.println(a) ;
      // byte b = 234324  ;
      // System.out.println(b) ;  error due to number is too long to store 

      float c = 3.45F ; 
      System.out.println(c) ;

      long d = 33423432432432L;
      System.out.println(d) ;
  }
}